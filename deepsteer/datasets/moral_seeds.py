def get_moral_seeds(
    foundation: MoralFoundation | None = None,
) -> dict[MoralFoundation, list[str]]:
    """Return a filtered copy of the moral seed sentences.

    Args:
        foundation: If provided, return only the seeds for that foundation.
            If ``None``, return seeds for all six foundations.

    Returns:
        A new dict mapping each requested :class:`MoralFoundation` to a
        list of declarative moral sentences.
    """
    if foundation is not None:
        return {foundation: list(MORAL_SEEDS[foundation])}
    return {f: list(seeds) for f, seeds in MORAL_SEEDS.items()}
